#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;
	int length;

	length = ft_strlen(s);
	if (c == 0)
		return ((char*)&s[length]);
	i = length - 1;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i--;
	}
	return (NULL);
}
