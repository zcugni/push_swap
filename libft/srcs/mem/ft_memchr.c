#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((t_u_char *)s)[i] == (t_u_char)c)
			return (&((t_u_char *)s)[i]);
		i++;
	}
	return (NULL);
}
