#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int i;

	i = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
			return ((t_u_char)s1[i] - (t_u_char)s2[i]);
		i++;
	}
	return (0);
}
